def read_fasta_records(fasta_path: Path, model_name: str) -> list[DesignedSequence]:
    """Parse the FASTA file written by a ProteinMPNN runner."""

    records: list[DesignedSequence] = []
    current_header: Optional[str] = None
    current_sequence_parts: list[str] = []

    def flush_current_record() -> None:
        """Store the record currently being collected, if there is one."""

        if current_header is None:
            return

        sequence = "".join(current_sequence_parts)
        kind = "design" if current_header.startswith("T=") else "native"

        records.append(
            DesignedSequence(
                model=model_name,
                kind=kind,
                sample=header_int(current_header, "sample"),
                score=header_float(current_header, "score"),
                global_score=header_float(current_header, "global_score"),
                seq_recovery=header_float(current_header, "seq_recovery"),
                header=current_header,
                sequence=sequence,
            )
        )

    with fasta_path.open("r", encoding="utf-8", errors="replace") as handle:
        for raw_line in handle:
            line = raw_line.strip()
            if not line:
                continue

            if line.startswith(">"):
                flush_current_record()
                current_header = line[1:]
                current_sequence_parts = []
            else:
                current_sequence_parts.append(line)

    flush_current_record()
    return records


def first_fasta_in_output(output_folder: Path) -> Optional[Path]:
    """Find the first FASTA file generated under <out_folder>/seqs."""

    seq_dir = output_folder / "seqs"
    if not seq_dir.exists():
        return None

    candidates = sorted(seq_dir.glob("*.fa")) + sorted(seq_dir.glob("*.fasta"))
    return candidates[0] if candidates else None


def best_design(records: list[DesignedSequence]) -> Optional[DesignedSequence]:
    """Pick the most useful design for a quick side-by-side comparison."""

    designs = [record for record in records if record.kind == "design"]
    if not designs:
        return None

    # Sequence recovery is the easiest "correctness" number for a demo because
    # it compares the designed sequence to the native PDB sequence. Higher is
    # better. If a runner did not report recovery, fall back to score, where
    # lower is better.
    designs_with_recovery = [record for record in designs if record.seq_recovery is not None]
    if designs_with_recovery:
        return max(designs_with_recovery, key=lambda record: record.seq_recovery or -1.0)

    designs_with_score = [record for record in designs if record.score is not None]
    if designs_with_score:
        return min(designs_with_score, key=lambda record: record.score or float("inf"))

    return designs[0]


def records_to_table(records: list[DesignedSequence]) -> pd.DataFrame:
    """Convert FASTA records into a table for st.dataframe."""

    return pd.DataFrame(
        [
            {
                "Model": record.model,
                "Vrsta": "Predlog" if record.kind == "design" else "Izvirno zaporedje",
                "Vzorec": record.sample,
                "Ujemanje (%)": None
                if record.seq_recovery is None
                else round(record.seq_recovery * 100, 1),
                "Zaporedje": record.sequence,
            }
            for record in records
        ]
    )



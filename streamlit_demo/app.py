@dataclass
class DesignedSequence:
    """One FASTA record generated by a model run."""

    model: str
    kind: str
    sample: Optional[int]
    score: Optional[float]
    global_score: Optional[float]
    seq_recovery: Optional[float]
    header: str
    sequence: str


@dataclass
class ModelRun:
    """Everything the UI needs to know about one model execution."""

    model: str
    return_code: int
    elapsed_seconds: float
    command: list[str]
    output_folder: Path
    fasta_path: Optional[Path]
    records: list[DesignedSequence]
    stdout: str
    stderr: str

    @property
    def ok(self) -> bool:
        """A run is considered successful if the process exited with code 0."""

        return self.return_code == 0



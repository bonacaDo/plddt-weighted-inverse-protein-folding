def run_model_command(model_name: str, command: list[str], output_folder: Path) -> ModelRun:
    """Run one model command and parse its FASTA output."""

    start = time.perf_counter()

    try:
        completed = subprocess.run(
            command,
            cwd=REPO_ROOT,
            text=True,
            capture_output=True,
            timeout=COMMAND_TIMEOUT_SECONDS,
            check=False,
        )
        return_code = completed.returncode
        stdout = completed.stdout
        stderr = completed.stderr
    except subprocess.TimeoutExpired as exc:
        return_code = -1
        stdout = exc.stdout or ""
        stderr = (exc.stderr or "") + f"\nTimed out after {COMMAND_TIMEOUT_SECONDS} seconds."

    elapsed = time.perf_counter() - start
    fasta_path = first_fasta_in_output(output_folder)
    records = read_fasta_records(fasta_path, model_name) if fasta_path else []

    return ModelRun(
        model=model_name,
        return_code=return_code,
        elapsed_seconds=elapsed,
        command=command,
        output_folder=output_folder,
        fasta_path=fasta_path,
        records=records,
        stdout=stdout,
        stderr=stderr,
    )


def save_uploaded_pdb(uploaded_file) -> Path:
    """Save an uploaded Streamlit file to outputs/streamlit_demo/uploads."""

    UPLOAD_DIR.mkdir(parents=True, exist_ok=True)

    stamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    unique = uuid.uuid4().hex[:8]
    file_name = safe_file_name(uploaded_file.name)
    target = UPLOAD_DIR / f"{stamp}_{unique}_{file_name}"

    target.write_bytes(uploaded_file.getbuffer())
    return target


def path_from_text(text: str) -> Path:
    """Resolve a path typed in the sidebar.

    Absolute paths are used as-is. Relative paths are interpreted relative to
    the repository root, which is friendlier when the user types something like
    demo_cases/AF-P15502-F1.pdb.
    """

    path = Path(text).expanduser()
    return path if path.is_absolute() else REPO_ROOT / path


def make_run_root(pdb_path: Path) -> Path:
    """Create a unique output folder for one comparison run."""

    stamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    unique = uuid.uuid4().hex[:8]
    run_root = RUN_DIR / f"{pdb_path.stem}_{stamp}_{unique}"
    run_root.mkdir(parents=True, exist_ok=True)
    return run_root



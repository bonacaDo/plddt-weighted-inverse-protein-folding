def build_vanilla_command(
    pdb_path: Path,
    output_folder: Path,
    num_sequences: int,
    temperature: str,
    chain_text: str,
) -> list[str]:
    """Build the command for the original ProteinMPNN model."""

    command = [
        sys.executable,
        str(VANILLA_RUNNER),
        "--pdb_path",
        str(pdb_path),
        "--out_folder",
        str(output_folder),
        "--num_seq_per_target",
        str(num_sequences),
        "--batch_size",
        "1",
        "--sampling_temp",
        temperature,
        "--path_to_model_weights",
        str(VANILLA_WEIGHTS),
        "--model_name",
        VANILLA_MODEL_NAME,
        "--seed",
        str(DEMO_SEED),
    ]

    if chain_text:
        command.extend(["--pdb_path_chains", chain_text])

    return command



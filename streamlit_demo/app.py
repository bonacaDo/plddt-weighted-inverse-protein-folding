def normalized_sequence(sequence: str) -> str:
    """Remove separators such as '/' so sequences align with PDB positions."""

    return "".join(character for character in sequence if character.isalpha())


def native_record(records: list[DesignedSequence]) -> Optional[DesignedSequence]:
    """Return the native FASTA record if a model runner wrote one."""

    return next((record for record in records if record.kind == "native"), None)


def confidence_for_bins(confidence_table: pd.DataFrame, data_source: str) -> pd.Series:
    """Return a 0-100 confidence-like value for binning.

    AlphaFold files already store pLDDT on a 0-100 scale. Experimental PDB files
    store B-factors instead, so for display we convert them into a relative
    confidence signal: lower B-factor becomes higher confidence.
    """

    values = confidence_table["vrednost"].astype(float)
    if data_source != "xray":
        return values

    min_value = values.min()
    max_value = values.max()
    if max_value == min_value:
        return pd.Series([100.0] * len(values), index=values.index)

    return 100.0 * (max_value - values) / (max_value - min_value)



def parse_pdb_summary(pdb_path: Path, selected_chains: list[str]) -> tuple[dict, pd.DataFrame]:
    """Read a tiny summary from a PDB without depending on a biology library.

    The actual model runner does its own proper parsing. This helper is only for
    the app preview: number of residues, chains, and the B-factor/pLDDT trend
    from CA atoms.
    """

    selected = set(selected_chains)
    atom_lines = 0
    chains_seen: set[str] = set()
    residues_seen: set[tuple[str, str, str]] = set()
    ca_by_residue: dict[tuple[str, str, str], dict] = {}
    ca_order = 0

    with pdb_path.open("r", encoding="utf-8", errors="replace") as handle:
        for line in handle:
            if not line.startswith("ATOM"):
                continue

            # These columns are part of the PDB fixed-width format.
            atom_name = line[12:16].strip()
            altloc = line[16].strip()
            residue_name = line[17:20].strip()
            chain_id = line[21].strip() or "_"
            residue_number = line[22:26].strip()
            insertion_code = line[26].strip()

            # If the user chose chains, the preview focuses on those chains.
            if selected and chain_id not in selected:
                continue

            atom_lines += 1
            chains_seen.add(chain_id)
            residue_key = (chain_id, residue_number, insertion_code)
            residues_seen.add(residue_key)

            # The confidence value we care about is conventionally read from the
            # B-factor column. For AlphaFold this is pLDDT; for X-ray structures
            # it is a crystallographic B-factor.
            if atom_name == "CA":
                try:
                    x = float(line[30:38])
                    y = float(line[38:46])
                    z = float(line[46:54])
                    bfactor = float(line[60:66])
                except ValueError:
                    continue

                # Some PDB files contain alternate conformations for the same
                # residue. For a public demo, counting all of them is confusing,
                # so we keep one representative CA atom per residue:
                # blank altloc is best, "A" is next, then anything else.
                altloc_priority = {"": 0, "A": 1}.get(altloc, 2)
                existing = ca_by_residue.get(residue_key)
                if existing is None or altloc_priority < existing["_altloc_priority"]:
                    ca_by_residue[residue_key] = {
                        "order": existing["order"] if existing else ca_order,
                        "_altloc_priority": altloc_priority,
                        "chain": chain_id,
                        "residue": f"{chain_id}{residue_number}{insertion_code}",
                        "residue_name": residue_name,
                        "x": x,
                        "y": y,
                        "z": z,
                        "bfactor_or_plddt": bfactor,
                    }
                    if existing is None:
                        ca_order += 1

    ca_rows = []
    for position, row in enumerate(
        sorted(ca_by_residue.values(), key=lambda item: item["order"]), start=1
    ):
        ca_rows.append(
            {
                "pozicija": position,
                "veriga": row["chain"],
                "ostanek": row["residue"],
                "aminokislina": row["residue_name"],
                "x": row["x"],
                "y": row["y"],
                "z": row["z"],
                "vrednost": row["bfactor_or_plddt"],
            }
        )

    summary = {
        "atom_lines": atom_lines,
        "chains": ", ".join(sorted(chains_seen)) if chains_seen else "none",
        "residue_count": len(residues_seen),
        "ca_count": len(ca_rows),
    }

    return summary, pd.DataFrame(ca_rows)



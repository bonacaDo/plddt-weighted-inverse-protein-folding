def comparison_sentence(
    baseline: Optional[DesignedSequence], plddt: Optional[DesignedSequence]
) -> str:
    """Summarize which model did better on sequence recovery."""

    if baseline is None or plddt is None:
        return "Primerjava ni popolna, ker eden od modelov ni vrnil predloga."

    if baseline.seq_recovery is None or plddt.seq_recovery is None:
        return "Modela sta vrnila zaporedja, vendar v izhodu ni bilo podatka o ujemanju."

    difference = (plddt.seq_recovery - baseline.seq_recovery) * 100
    if difference > 0:
        return f"Na tem primeru je model z utežmi zanesljivosti dosegel za {difference:.1f} odstotne točke boljše ujemanje z izvirnim zaporedjem."
    if difference < 0:
        return f"Na tem primeru je osnovni model dosegel za {abs(difference):.1f} odstotne točke boljše ujemanje. To je še vedno uporaben rezultat, ker vidimo, kje pristop z utežmi zanesljivosti pomaga in kje ne."
    return "Na tem primeru sta modela dosegla enako ujemanje z izvirnim zaporedjem."



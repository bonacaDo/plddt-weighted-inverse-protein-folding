def safe_file_name(name: str) -> str:
    """Return a filesystem-friendly version of an uploaded file name."""

    # Keep normal letters, numbers, dots, underscores, and hyphens. Everything
    # else becomes "_", which avoids surprises from spaces or shell characters.
    return re.sub(r"[^A-Za-z0-9._-]+", "_", name).strip("_") or "uploaded.pdb"


def chain_list_from_text(text: str) -> list[str]:
    """Turn a sidebar chain field like 'A B' or 'A,B' into ['A', 'B']."""

    if not text.strip():
        return []

    # The ProteinMPNN script expects chains as a space-separated string, but for
    # our PDB summary it is more convenient to keep them as a Python list too.
    parts = re.split(r"[\s,]+", text.strip())
    return [part for part in parts if part]


def chain_text_for_cli(chains: Iterable[str]) -> str:
    """Turn a chain list back into the format expected by ProteinMPNN."""

    return " ".join(chains)


def header_value(header: str, key: str) -> Optional[str]:
    """Read a value like score=1.23 from a FASTA header line."""

    # The negative lookbehind prevents key="score" from matching inside
    # "global_score". Values end at a comma or whitespace.
    pattern = rf"(?<![A-Za-z0-9_]){re.escape(key)}=([^,\s]+)"
    match = re.search(pattern, header)
    return match.group(1) if match else None


def header_float(header: str, key: str) -> Optional[float]:
    """Read a float value from a FASTA header if it is present."""

    value = header_value(header, key)
    if value is None:
        return None
    try:
        return float(value)
    except ValueError:
        return None


def header_int(header: str, key: str) -> Optional[int]:
    """Read an integer value from a FASTA header if it is present."""

    value = header_value(header, key)
    if value is None:
        return None
    try:
        return int(value)
    except ValueError:
        return None


def command_as_text(command: list[str]) -> str:
    """Format a subprocess command so it is easy to inspect in the UI."""

    return shlex.join(command)



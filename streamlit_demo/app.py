def per_bin_recovery_rows(
    native: Optional[DesignedSequence],
    design: Optional[DesignedSequence],
    model_label: str,
    confidence_table: pd.DataFrame,
    data_source: str,
) -> list[dict]:
    """Compute sequence recovery inside each confidence bin."""

    if native is None or design is None or confidence_table.empty:
        return []

    native_sequence = normalized_sequence(native.sequence)
    design_sequence = normalized_sequence(design.sequence)
    confidence_values = list(confidence_for_bins(confidence_table, data_source))
    usable_length = min(len(native_sequence), len(design_sequence), len(confidence_values))
    if usable_length == 0:
        return []

    rows = []
    for label, low, high in CONFIDENCE_BINS:
        indices = [
            index
            for index, value in enumerate(confidence_values[:usable_length])
            if low <= value < high
        ]
        if not indices:
            continue

        matches = sum(
            1 for index in indices if native_sequence[index] == design_sequence[index]
        )
        rows.append(
            {
                "Razred zanesljivosti": label,
                "Model": model_label,
                "Pozicije": len(indices),
                "Ujemanje (%)": 100.0 * matches / len(indices),
            }
        )

    return rows


